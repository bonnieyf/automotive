function updateContent() {
  console.log("update");
  let elem = document.querySelectorAll("[data-i18n]");
  elem.forEach((item) => {
    item.innerHTML = i18next.t(item.dataset.i18n);
  });
}

function changeLang(lng) {
  i18next.changeLanguage(lng);
}

i18next.on("languageChanged", () => {
  updateContent();
});

function getParameterByName(name, url = window.location.href) {
  name = name.replace(/[\[\]]/g, "\\$&");
  var regex = new RegExp("[?&]" + name + "(=([^&#]*)|&|#|$)"),
    results = regex.exec(url);
  if (!results) return null;
  if (!results[2]) return "";

  return decodeURIComponent(results[2].replace(/\+/g, " ")).substr(0, 2);
}

function MultiLanguage() {
  unityInstance.SendMessage(
    "Language_Manager",
    "SendToUnity",
    window.localStorage.i18nextLng === "zh"
      ? "cn"
      : window.localStorage.i18nextLng || "en"
  );
  unityInstance.SendMessage("Language_Manager", "loadLanguage");
}


function isMobileDevice() {
  const mobileDevice = [
    "Android",
    "webOS",
    "iPhone",
    "iPad",
    "iPod",
    "BlackBerry",
    "Windows Phone",
  ];
  let isMobileDevice = mobileDevice.some((e) => navigator.userAgent.match(e));
  return isMobileDevice;
}
